function mostrarCoringa () {
    var
    divCartaz = document.getElementById ("cartazCoringa").style.display = "none",
    divTexto = document.getElementById ("textoCoringa").style.display = "none";

    if (divCartaz == "none" && divTexto == "none") {
        document.getElementById ("cartazCoringa").style.display = "block";
        document.getElementById ("textoCoringa").style.display = "block";
    }
}

function fecharCoringa () {
    var
    divCartaz2 = document.getElementById ("cartazCoringa").style.display = "block",
    divTexto2 = document.getElementById ("textoCoringa").style.display = "block";

    if (divCartaz2 == "block" && divTexto2 == "block"){
        document.getElementById ("cartazCoringa").style.display = "none";
        document.getElementById ("textoCoringa").style.display = "none";
    }
}

